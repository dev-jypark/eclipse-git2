package com.kosmo.fridge.service;

public class IngredientDTO {
	
	private String i_no;
	private String id;
	private String i_name;
	private String i_cnt;
	private String i_enddate;
	
	
	public String getI_no() {
		return i_no;
	}
	public void setI_no(String i_no) {
		this.i_no = i_no;
	}
	public String getId() {
		return id;
	}
	public void setId(String id) {
		this.id = id;
	}
	public String getI_name() {
		return i_name;
	}
	public void setI_name(String i_name) {
		this.i_name = i_name;
	}
	public String getI_cnt() {
		return i_cnt;
	}
	public void setI_cnt(String i_cnt) {
		this.i_cnt = i_cnt;
	}
	public String getI_enddate() {
		return i_enddate;
	}
	public void setI_enddate(String i_enddate) {
		this.i_enddate = i_enddate;
	}
	
	

}
